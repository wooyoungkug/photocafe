export function getRandomCard(): TarotCard & { isReversed: boolean } {
  const randomIndex = Math.floor(Math.random() * majorArcana.length);
  const isReversed = Math.random() > 0.5;
  return {
    ...majorArcana[randomIndex],
    isReversed
  };
}

export function getRandomCards(count: number): (TarotCard & { isReversed: boolean })[] {
  const shuffled = [...majorArcana].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count).map(card => ({
    ...card,
    isReversed: Math.random() > 0.5
  }));
}
